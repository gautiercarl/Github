package Praktikum5;

public class IncrementDemo {
    public static void main(String[] args) {
        for (int i = 1; i < 11; i++) {
            System.out.println(i);
        }

        int i = 1;
        while (i < 11) {
            System.out.println(i);
            i++;
        }

        for (int a = -3; a < 4; a++) {
            System.out.println(a);
        }

        int a = -3;
        while (a< 4) {
            System.out.println(a);
            a++;
        }

    }

}
