package Praktikum5;

public class DecrementDemo {
    public static void main(String[] args) {
        for (int i = 10; i >=0; i--) {
            System.out.println(i);
        }

        int i = 10;
        while (i>=0) {
            System.out.println(i);
            i--;
        }

        for (int a = 0; a >=-5; a--) {
            System.out.println(a);
        }

        int a = 0;
        while (a>= -5) {
            System.out.println(a);
            a--;
        }  
    }
}
